package com.Arrays;

public class DiffrenceWayPrintArrays {

	public static void main(String[] args) {
		
		int arr[]= {10,20,30,40,50,60,70,80,90};
		
		for(int x: arr)
		{
			System.out.println(x);
		}
		
		for(int i=0; i<arr.length; i++)
		{
			System.out.println(arr[i]);
		}
		
		System.out.println(arr[0]);
	}
}
